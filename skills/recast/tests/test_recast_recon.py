"""Contract tests for recast-recon.sh — comprehensive default, --traces-only, path:line only."""

import re

import pytest
from recast_helpers import RECON_SH, run

PATHLINE = re.compile(r"\S+:\d+")


def test_clean_tree(tmp_path):
    """A marker-free tree exits 0."""
    tree = tmp_path / "tree"
    tree.mkdir()
    (tree / "a.txt").write_text("hello world\n")
    (tree / "b.txt").write_text("nothing to see here\n")

    res = run(RECON_SH, tree)
    assert res.returncode == 0, res.stderr


def test_ai_marker_present(tmp_path):
    """A trace marker hit exits 1, prints path:line, never the content."""
    tree = tmp_path / "tree"
    tree.mkdir()
    (tree / "msg.txt").write_text("subject line\n\nCo-Authored-By: someone\n")

    res = run(RECON_SH, tree)
    assert res.returncode == 1, res.stderr
    assert PATHLINE.search(res.stdout), res.stdout
    assert "Co-Authored-By" not in res.stdout


def test_custom_pattern_matches(tmp_path):
    """A custom pattern from a positional file hits; content stays hidden."""
    tree = tmp_path / "tree"
    tree.mkdir()
    (tree / "leak.txt").write_text("token is SECRET123 do not share\n")

    pattern_file = tmp_path / "patterns.txt"
    pattern_file.write_text("SECRET[0-9]+\n")

    res = run(RECON_SH, tree, pattern_file)
    assert res.returncode == 1, res.stderr
    assert PATHLINE.search(res.stdout), res.stdout
    assert "SECRET123" not in res.stdout


def test_comments_only_pattern_file(tmp_path):
    """A comments/blank-only pattern file must not trigger the empty grep -f footgun."""
    tree = tmp_path / "tree"
    tree.mkdir()
    (tree / "a.txt").write_text("plain content\n")

    pattern_file = tmp_path / "patterns.txt"
    pattern_file.write_text("# just a comment\n\n   \n# another\n")

    res = run(RECON_SH, tree, pattern_file)
    assert res.returncode == 0, res.stderr
    assert res.stdout == ""


TRACE_FORMS = [
    "Co-Authored-By: someone\n",
    "co-authored by someone\n",  # space-form prose, not the hyphenated trailer
    "generated by a tool\n",
    "this is AI-assisted work\n",
    "\U0001f916 Generated with [Claude Code]\n",  # emoji + "generated with" footer
    "\U0001f916\n",  # pure emoji
]


@pytest.mark.parametrize("content", TRACE_FORMS)
def test_trace_forms_flagged_by_default(tmp_path, content):
    """Generation traces halt the gate; content stays hidden."""
    tree = tmp_path / "tree"
    tree.mkdir()
    (tree / "f.txt").write_text(content)

    res = run(RECON_SH, tree)
    assert res.returncode == 1, res.stderr
    assert PATHLINE.search(res.stdout), res.stdout


