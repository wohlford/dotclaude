def test_utf16le_content_marker_caught(tmp_path):
    """A provenance marker in a UTF-16LE file must NOT slip the scrub open."""
    tree = tmp_path / "tree"
    tree.mkdir()
    (tree / "notes.md").write_bytes(
        "Co-Authored-By: Claude <noreply@anthropic.com>\n".encode("utf-16-le")
    )
    res = run(RECON_SH, tree)
    assert res.returncode == 1, res.stderr
    assert ":wide" in res.stdout
    assert "Claude" not in res.stdout  # path-only, never content


